#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	size_t	i;

	i = 0;
	if (!dst && !src)
		return (NULL);
	if (dst > src)
		return (ft_memcpy(dst, src, len));
	else
		while (len--)
			*(char *)(dst + i++) = *(char *)src++;
	return (dst);
}
